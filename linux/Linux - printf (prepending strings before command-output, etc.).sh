#!/bin/sh
#
# ------------------------------------------------------------

find "/var/log/" \
-type f \
-iname syslog* \
-exec printf "$(date +'%Y-%m-%d %H:%M:%S') $(whoami)@$(hostname) | " \; \
-exec echo "------------------------------------------------------------" \; \
-exec cat '{}' \; \
-exec echo "------------------------------------------------------------" \; \
;

